package operator;

public class Operator4 {
    public static void main(String[] args) {
        int sum3 = 2 * 2 + 3 * 3;
        int sum4 = (2 * 2) + (3 * 3); //sum3과 같지만 괄호를 사용해주는게 명확하고 단순해서 코드 보기 좋다.
        System.out.println("sum3 = " + sum3);
        System.out.println("sum4 = " + sum4);

        /*
        우선순위 1. 상식선에서 생각, 2. 애메하면 괄호넣기 << 외우기 X
         */
    }
}
